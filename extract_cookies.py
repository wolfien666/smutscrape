def parse_args():
    p = argparse.ArgumentParser(
        description="Export Chrome cookies to Netscape format for use with yt-dlp / smutscrape."
    )
    p.add_argument(
        '--output', '-o',
        default=os.path.expanduser('~/smutscrape-cookies.txt'),
        help='Path to write the cookies.txt file (default: ~/smutscrape-cookies.txt)'
    )
    p.add_argument(
        '--domains', '-d',
        nargs='+',
        metavar='DOMAIN',
        default=None,
        help=(
            'Only export cookies for these domains. '
            'Omit to export cookies for all known smutscrape sites. '
            'Use --domains ALL to export every single cookie in your Chrome profile.'
        )
    )
    p.add_argument(
        '--profile', '-p',
        default='Default',
        help='Chrome profile directory name (default: Default). Use "Profile 1", "Profile 2", etc. for other profiles.'
    )
    p.add_argument(
        '--browser', '-b',
        choices=['chrome', 'chromium', 'brave', 'edge', 'firefox'],
        default='chrome',
        help='Browser to extract cookies from (default: chrome)'
    )
    return p.parse_args()


def load_cookies(browser, profile):
    """Return a cookiejar for the requested browser."""
    loaders = {
        'chrome':   browser_cookie3.chrome,
        'chromium': browser_cookie3.chromium,
        'brave':    browser_cookie3.brave,
        'edge':     browser_cookie3.edge,
        'firefox':  browser_cookie3.firefox,
    }
    loader = loaders.get(browser)
    if loader is None:
        print(f"[ERROR] Unknown browser '{browser}'.")
        sys.exit(1)

    print(f"[INFO] Loading cookies from {browser} (profile: {profile}) ...")
    try:
        # browser_cookie3 accepts cookie_file for custom path; easiest to let it auto-detect
        jar = loader()
        return jar
    except PermissionError:
        print(
            "[ERROR] Could not read Chrome's cookie database — permission denied.\n"
            "  Make sure Chrome is CLOSED before running this script."
        )
        sys.exit(1)
    except Exception as exc:
        print(f"[ERROR] Failed to load cookies: {exc}")
        sys.exit(1)


def filter_cookies(jar, domains):
    """Filter cookiejar to only cookies whose domain matches one of `domains`."""
    if domains is None:
        # Default: all smutscrape-relevant domains
        target = set(ALL_SMUT_DOMAINS)
        filtered = [
            c for c in jar
            if any(
                c.domain.lstrip('.').endswith(d) or d.endswith(c.domain.lstrip('.'))
                for d in target
            )
        ]
    elif len(domains) == 1 and domains[0].upper() == 'ALL':
        filtered = list(jar)
    else:
        target = set(d.lstrip('.') for d in domains)
        filtered = [
            c for c in jar
            if any(
                c.domain.lstrip('.').endswith(d) or d.endswith(c.domain.lstrip('.'))
                for d in target
            )
        ]
    return filtered


def write_netscape(cookies, output_path):
    os.makedirs(os.path.dirname(os.path.abspath(output_path)), exist_ok=True)
    with open(output_path, 'w', encoding='utf-8') as f:
        f.write(NETSCAPE_HEADER)
        for c in cookies:
            try:
                line = cookie_to_netscape_line(c)
                f.write(line + '\n')
            except Exception as e:
                # Skip any malformed cookie silently
                pass
    return len(cookies)


def main():
    args = parse_args()

    jar      = load_cookies(args.browser, args.profile)
    cookies  = filter_cookies(jar, args.domains)
    count    = write_netscape(cookies, args.output)

    print(f"[OK] Exported {count} cookies to: {args.output}")
    print()
    print("Next step — add this line to your smutscrape config.yaml:")
    print(f"  cookies_file: \"{args.output}\"")
    print()
    print("Or for a specific site only, add it under the site key in config.yaml:")
    print("  xhamster:")
    print(f"    cookies_file: \"{args.output}\"")
    print()
    print("[REMINDER] Keep this file private — never commit it to git.")


if __name__ == '__main__':
    main()
