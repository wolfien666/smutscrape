#!/usr/bin/env python3
"""
extract_cookies.py
==================
Extracts cookies from your local Google Chrome profile and writes them to a
Netscape-format cookies.txt file that yt-dlp (and smutscrape) can consume
directly via the `cookies_file` config key.

Usage
-----
    python extract_cookies.py
    python extract_cookies.py --output ~/smutscrape-cookies.txt
    python extract_cookies.py --output ~/smutscrape-cookies.txt --domains xhamster.com pornhub.com

Requirements
------------
    pip install browser-cookie3

Notes
-----
- Chrome must be CLOSED (or at least not actively writing) when you run this,
  otherwise the SQLite DB may be locked.
- On Linux the cookies are stored in an encrypted keyring (libsecret / gnome-keyring).
  If decryption fails, browser-cookie3 will still try to export with empty passwords;
  most session cookies will export fine either way.
- On Windows, Chrome cookies are encrypted with DPAPI and browser-cookie3 handles
  this automatically.
- The script NEVER transmits your cookies anywhere.  It only reads the local
  Chrome SQLite database and writes a plain-text Netscape cookies.txt to disk.
"""

import argparse
import os
import sys
import datetime

# ---------------------------------------------------------------------------
# Dependency check
# ---------------------------------------------------------------------------
try:
    import browser_cookie3
except ImportError:
    print(
        "[ERROR] browser-cookie3 is not installed.\n"
        "  Install it with:  pip install browser-cookie3"
    )
    sys.exit(1)


# ---------------------------------------------------------------------------
# Netscape cookie-file format helpers
# ---------------------------------------------------------------------------

NETSCAPE_HEADER = """# Netscape HTTP Cookie File
# This file was generated by extract_cookies.py for use with yt-dlp / smutscrape.
# Do NOT commit this file to version control.
"""

def _bool(val):
    return "TRUE" if val else "FALSE"

def _ts(cookie):
    """Return expiry as a Unix timestamp integer, or a far-future value."""
    exp = getattr(cookie, 'expires', None)
    if exp is None:
        # Session cookie — give it 10 years so yt-dlp doesn't discard it
        return int((datetime.datetime.now() + datetime.timedelta(days=3650)).timestamp())
    try:
        return int(exp)
    except (TypeError, ValueError):
        return int((datetime.datetime.now() + datetime.timedelta(days=3650)).timestamp())

def cookie_to_netscape_line(c):
    """
    Format one http.cookiejar.Cookie as a Netscape cookies.txt line.
    Columns: domain  include_subdomains  path  secure  expiry  name  value
    """
    domain = c.domain or ''
    # Netscape format: domain starting with '.' means include-subdomains = TRUE
    include_sub = _bool(domain.startswith('.'))
    path   = c.path or '/'
    secure = _bool(c.secure)
    expiry = _ts(c)
    name   = c.name  or ''
    value  = c.value or ''
    return f"{domain}\t{include_sub}\t{path}\t{secure}\t{expiry}\t{name}\t{value}"


